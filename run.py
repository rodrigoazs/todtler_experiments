def cll(posProb, negProb):
    llSum = 0
    for prob in posProb:
        if prob == 0:
            prob = 1e-6
        llSum += math.log(prob)
    for prob in negProb:
        if prob == 1:
            prob = 1 - 1e-6
        llSum += math.log(1 - prob)
    return llSum/(len(posProb) + len(negProb))

